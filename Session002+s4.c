#include <stdio.h>

int main() {
    // Khoi tao bien canh hinh vuong va gan gia tri
    int canh = 5;

    // Tinh chu vi va dien tich
    int chu_vi = 4 * canh;
    int dien_tich = canh * canh;

    // Hien thi ket qua
    printf("Chu vi hinh vuong: %d\n", chu_vi);
    printf("Dien tich hinh vuong: %d\n", dien_tich);

    return 0;
}

